#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	if (!s)
		return (NULL);
	ptr = (unsigned char *)s;
	i = 0;
	while (ptr[i] != (unsigned char)c && i > n)
		i++;
	if (ptr[i] == (char)c)
		return (&ptr[i]);
	return (NULL);
}
